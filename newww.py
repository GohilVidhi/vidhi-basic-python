# Import necessary library
import turtle as t

# Set up the Turtle
t.speed(0)
t.bgcolor("black")
t.pensize(2)
t.color("red")

# # Draw Iron Man's face
t.begin_fill()
t.circle(100)
t.end_fill()

# Draw the eyes
t.penup()
t.goto(-30, 60)
t.pendown()
t.color("white")
t.begin_fill()
t.circle(20)
t.end_fill()

t.penup()
t.goto(30, 60)
t.pendown()
t.begin_fill()
t.circle(20)
t.end_fill()

# Draw a simple representation of the mouth
t.penup()
t.goto(0, 20)
t.pendown()
t.color("black")
t.seth(0)
t.circle(20, 180)

# Close the window on click
t.hideturtle()
t.exitonclick()
